# operadores aritméticos (continuação)
valor01 = 8
valor02 = 11

print("Exponenciação: ", valor01**3)
print("Divisão: ", valor01/valor02)
print("Divisão sem decimais", valor01//valor02)
print("Modulo(resto) da divisão: ", valor02%valor01)

# operadores de comparação
print("Valor 1 é maior que o valor 2:",valor01>valor02)
print("Valor 1 é maior ou igual ao valor 2:", valor01>=valor02)
print("valor 1 é menor que o valor 2: ", valor01<valor02)
print("Valor 1 é menor ou igual ao valor 2:",valor01<=valor02)
print("Valor 1 é igual ao valor 2:", valor01==valor02)
print("Valor 1 é diferente do valor 2:",valor01!=valor02)
